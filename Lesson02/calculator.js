let x = 10;
let y = 5;

add(x, y);
subtract(x, y);
multiply(x, y);
divide(x, y);

function add(x, y) {
    console.log(x + y);
    //15
}

function subtract(x, y) {
    console.log(x-y);
    //5
}

function multiply(x, y) {
    console.log(x * y);
    //50
}

function divide(x, y) {
    console.log(x / y);
    //2
}